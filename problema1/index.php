	<h2>1. Cuenta corriente</h2>
	<p> Una cuenta corriente es un tipo de depósito a la vista que permite al cliente retirar el dinero en 
	cualquier momento y sin preaviso. Suele utilizarse como cuenta central para operaciones bancarias del día 
	a día, como ingresos, pagos, domiciliaciones de recibos, transferencias o retiradas de efectivo en cajeros 
	automáticos, entre otras. También se conoce como depósito a la vista.</p>
	<p>Las cuentas corrientes pueden tener uno o varios titulares y, por tanto, ser usadas por más de una persona.</p>
	
	<h2>2. Cuenta ahorro</h2>
	<p> Una cuenta ahorro es una modalidad de depósito bancario a la vista que permite rentabilizar los ahorros, ya que el cliente deposita una cantidad de dinero en la entidad bancaria y esta, a cambio, le paga en función de los intereses generados. La rentabilidad obtenida vendrá determinada por el periodo de tiempo que el dinero se mantenga depositado en la entidad bancaria.</p>
	<p>Las cuentas de ahorro permiten compatibilizar la rentabilidad con la posibilidad de retirar el dinero en cualquier momento sin que suponga una penalización.</p>
	
	<h2>3. Cuenta joven</h2>
	<p> Una cuenta bancaria para jóvenes es una cuenta pensada para un público joven entre 18 y 31 años.  Normalmente, estas cuentas comparten unas ventajas asociadas comunes, como la exención de comisiones o vinculaciones, la no exigencia de un ingreso mínimo para su apertura, la tarjeta de débito gratuita, el carné joven asociado y múltiples promociones, descuentos y ventajas en partners de ocio, restauración, etc.</p>
